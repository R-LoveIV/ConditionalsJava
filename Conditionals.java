
// write a conditional statement to decide if someone is of legal age to buy a drink.
public class Conditionals {
    public static void main(String[] args){
        int age = 29;

        if (age >= 21){
            System.out.println("You're old enough to buy a drink");
        } else {
            System.out.println("You're not old enough. Come back when you're 21");
        }
    }
}
